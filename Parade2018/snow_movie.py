groundstr = groundstr.replace('-','')
groundstr = groundstr.replace('\n','')
groundstr = groundstr.replace('.','0')

ground = []
for c in groundstr:
    ground.append(int(c))

from pixel_sign import SignFrame
import random
import pixel_text

def new_flakes(n):
    for _ in range(random.randint(n[0],n[1])):
        x = random.randint(0,63)
        c = random.randint(1,3)
        ground[x] = c                

with open('snowGEN','w') as ps:
    for i in range(120):
        
        frame = SignFrame()
                        
        for y in range(32):
            for x in range(64):
                c = ground[y*64+x]                
                frame.set_pixel(x,y,c)
        ps.write(frame.to_string())
        
        for x in range(0,64):
            for y in range(28,-1,-1):
                if ground[y*64+x]!=0 and ground[(y+1)*64+x]==0:
                    ground[(y+1)*64+x] = ground[y*64+x]
                    ground[y*64+x] = 0
        
        if i<100:
            new_flakes([5,8])
            
    y_merry = -26
    y_christ = 17 
    
    for _ in range(18):        
        frame = SignFrame()
        pixel_text.draw_string('simple7x13',frame,12,y_merry,'Merry',[11,12,13,14,15],0)
        pixel_text.draw_string('simple7x13',frame,1,y_christ,'Christmas',[16,17,18,19,20,21,22,23,24],0)        
        ps.write(frame.to_string())
        
        if y_merry<-10:
            y_merry += 1
        if y_christ>4:
            y_christ -= 1
        
    for _ in range(64*5):
        ps.write(frame.to_string())

